//获取一个数的第bit位 从1开始 
unsigned short valueAtBit(unsigned short num,int  bit) {
    return (num >> (bit-1)) & 1;
}
//对一个数做逐位两两异或
unsigned  short xorbit(unsigned short data){
	int i,j;
	unsigned short res;
	res=valueAtBit(data,16);
	for(j=15;j>0;j--){
		res=res^valueAtBit(data,j);
	}
	return res;
} 
//生成DDT 
void ddt(unsigned short *difm,unsigned short diftable[256][256],unsigned short *m1,unsigned char *S){
	unsigned short c1;
	unsigned short c2;
	unsigned short m2;
	unsigned short difc;
	int i,j;
	for(i=0;i<256;i++){
		for(j=0;j<256;j++){
			m2=m1[j]^difm[i];
			c1=S[m1[j]];
			c2=S[m2];
			difc=c1^c2;
			diftable[m1[i]][difc]+=1;
		}
	}
	
}
//生成LAT 
void lft(unsigned short *a,unsigned short *b,short lintable[256][256],unsigned short *m1,unsigned char *S){
	unsigned short m_a,m_a_xor;
	unsigned short c_b,c_b_xor;
	
	int i,j,k;
	//a遍历 
	for(i=0;i<256;i++){
		//每个a有256个b 
		for(j=0;j<256;j++){
			//每个a,b有256个m,c 
			for(k=0;k<256;k++){
				m_a=a[i]&m1[k];
				m_a_xor=xorbit(m_a);
				c_b=b[j]&S[m1[k]];//此时m对应的输出
				c_b_xor=xorbit(c_b);
				if(m_a_xor==c_b_xor){
					lintable[a[i]][b[j]]++;
				}			
			}
		lintable[a[i]][b[j]]=lintable[a[i]][b[j]]-128;		
		}
		
	} 
} 

